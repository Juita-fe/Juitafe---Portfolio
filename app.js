// Hamburger menu
function toggleMenu(e) {
  e.stopPropagation();
  const menu = document.getElementById("navMenu");
  menu.classList.toggle("open");
}

function closeMenu() {
  const menu = document.getElementById("navMenu");
  menu.classList.remove("open");
}

// Language dropdown
function toggleLangMenu(e) {
  e.stopPropagation();
  const dropdown = document.getElementById("langDropdown");
  dropdown.classList.toggle("open");
}

// Close dropdowns when clicking outside
document.addEventListener("click", function (e) {
  const langToggle = document.querySelector(".lang-toggle");
  const navMenu = document.getElementById("navMenu");
  const hamburger = document.querySelector(".hamburger");

  if (langToggle && !langToggle.contains(e.target)) {
    document.getElementById("langDropdown").classList.remove("open");
  }
  if (navMenu && !navMenu.contains(e.target) && !hamburger.contains(e.target)) {
    navMenu.classList.remove("open");
  }
});

// Set language
function setLang(lang, flag, label) {
  document.querySelectorAll("[data-lang-key]").forEach((el) => {
    const key = el.getAttribute("data-lang-key");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll("[data-lang-key-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-lang-key-placeholder");
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });
  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.classList.remove("active");
  });
  document.getElementById("current-flag").src = flag;
  document.getElementById("current-lang").textContent = label;
  setTimeout(() => {
    document.getElementById("langDropdown").classList.remove("open");
  }, 100);
  localStorage.setItem("lang", lang);
  localStorage.setItem("flag", flag);
  localStorage.setItem("label", label);
}

// DOM ready
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("lang") || "en";
  const savedFlag =
    localStorage.getItem("flag") || "https://flagcdn.com/w20/gb.png";
  const savedLabel = localStorage.getItem("label") || "EN";

  document.getElementById("current-flag").src = savedFlag;
  document.getElementById("current-lang").textContent = savedLabel;

  document.querySelectorAll("[data-lang-key]").forEach((el) => {
    const key = el.getAttribute("data-lang-key");
    if (translations[savedLang] && translations[savedLang][key]) {
      el.textContent = translations[savedLang][key];
    }
  });

  // Scroll animations
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
        }
      });
    },
    { threshold: 0.05 },
  );

  document.querySelectorAll(".service-card, section h2").forEach((el) => {
    observer.observe(el);
    if (el.getBoundingClientRect().top < window.innerHeight) {
      el.classList.add("visible");
    }
  });
});

// Smooth scroll
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    e.preventDefault();
    document.querySelector(this.getAttribute("href")).scrollIntoView({
      behavior: "smooth",
    });
  });
});

// Contact form
document.getElementById("contactForm").addEventListener("submit", function (e) {
  e.preventDefault();

  const name = this.querySelector('input[type="text"]').value;
  const email = this.querySelector('input[type="email"]').value;
  const message = this.querySelector("textarea").value;
  const btn = this.querySelector('button[type="submit"]');

  btn.textContent = "Sending...";
  btn.disabled = true;

  fetch("contact.php", {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: `name=${encodeURIComponent(name)}&email=${encodeURIComponent(email)}&message=${encodeURIComponent(message)}`,
  })
    .then((response) => {
      return response.text().then((text) => {
        return { status: response.status, text: text };
      });
    })
    .then((data) => {
      if (data.status === 200) {
        btn.textContent = "Message Sent!";
        btn.style.background = "var(--mint-mid)";
        this.reset();
        setTimeout(() => {
          btn.textContent = "Send Message";
          btn.disabled = false;
          btn.style.background = "";
        }, 3000);
      } else {
        btn.textContent = "Failed. Try Again.";
        btn.disabled = false;
      }
    })
    .catch((error) => {
      btn.textContent = "Failed. Try Again.";
      btn.disabled = false;
    });
});

// Navbar scroll effect
window.addEventListener("scroll", function () {
  const nav = document.querySelector("nav");
  if (window.scrollY > 50) {
    nav.style.boxShadow = "0 2px 20px rgba(0,0,0,0.15)";
  } else {
    nav.style.boxShadow = "none";
  }
});
